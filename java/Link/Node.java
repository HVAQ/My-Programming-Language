public class Node {
	// 链表的节点设置
	private String node;

	public Node() {}	// 无参构造
	
	public Node(String node) {
		this.node = node;
	}

	public void setNode(String node) {
		this.node = node;
	}

	public String getNode() {
		return this.node;
	}

	public String getNodeInfo() {
		return this.node;
	}
}
